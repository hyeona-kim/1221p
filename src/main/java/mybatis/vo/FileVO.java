package mybatis.vo;

public class FileVO {
	
	private String f_idx, f_name, f_info, c_idx;

	public String getF_idx() {
		return f_idx;
	}

	public void setF_idx(String f_idx) {
		this.f_idx = f_idx;
	}

	public String getF_name() {
		return f_name;
	}

	public void setF_name(String f_name) {
		this.f_name = f_name;
	}

	public String getF_info() {
		return f_info;
	}

	public void setF_info(String f_info) {
		this.f_info = f_info;
	}

	public String getC_idx() {
		return c_idx;
	}

	public void setC_idx(String c_idx) {
		this.c_idx = c_idx;
	}

}
